
"""
Вещественные числа

В Python числа, имеющие дробную часть, называют вещественными (или числами с плавающей точкой).
"""

print('Сложение дробного числа 0.1 + 0.1')
float_addition = 0.1 + 0.1
print(float_addition)

print('Умножение дробного числа 2 * 0.1')
float_multiplication = 2 * 0.1
print(float_multiplication)
